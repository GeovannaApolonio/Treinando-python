print("Calcule sua média no bimestre:")
notA = float(input("Insira sua primeira nota: "))
notB = float(input("Insira sua segunda nota: "))
notC = float(input("Insira sua terceira nota: "))
notD = float(input("Insira sua quarta nota: "))

med = (notA + notB + notC + notD)/4

print(f"Sua média final ficou {med}.")